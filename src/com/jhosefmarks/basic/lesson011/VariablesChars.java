package com.jhosefmarks.basic.lesson011;

public class VariablesChars {

	public static void main(String[] args) {

		char h = 'H'; // equal, char h = 72;
		char i = 'i'; // equal, char i = 105;
		char e = '!'; // equal, char e = 0x0021;
		
		System.out.println(h + i + e); // print numbers (ASCII)
		System.out.println("" + h + i + e); // convert and print characters

	}

}
