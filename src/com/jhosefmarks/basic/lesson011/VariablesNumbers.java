package com.jhosefmarks.basic.lesson011;

public class VariablesNumbers {

	public static void main(String[] args) {

		byte age1 = 20;
		short age2 = 35;
		int age3 = 45;
		long age4 = 60;
		
		System.out.println("Value of age1: " + age1);
		System.out.println("Value of age2: " + age2);
		System.out.println("Value of age3: " + age3);
		System.out.println("Value of age4: " + age4);
		
		float salary1 = 9504.87f;
		double salary2 = 82489.90;
		
		System.out.println("Value of salary1: " + salary1);
		System.out.println("Value of salary2: " + salary2);
	}

}
